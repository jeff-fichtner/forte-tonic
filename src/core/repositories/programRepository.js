class ProgramRepository {

  constructor(dbClient) {
    this.dbClient = dbClient;
  }

  initialize() {
    const classes = this.getClasses();
    ErrorHandling.throwIfNo(classes, `No classes found`);

    const registrations = this.getRegistrations();
    ErrorHandling.throwIfNo(registrations, `No registrations found`);
  }

  getClasses() {
    return RepositoryHelper.getAndSetData(
      () => this.classes,
      () => this.classes =
        this.dbClient.getAllRecords(
          Keys.CLASSES,
          x => new Class(...x)),
      Keys.CLASSES);
  }

  getClassById(id) {
    return this.getClasses().find(x => x.Id === id);
  }

  getRegistrations() {
    return RepositoryHelper.getAndSetData(
      () => this.registrations,
      () => this.registrations =
        this.dbClient.getAllRecords(
          Keys.REGISTRATIONS,
          x => {
            const newRegistration = new Registration(...x);
            newRegistration.startTime = DateHelpers.parseGoogleSheetsDate(newRegistration.startTime);
            return newRegistration;
          }),
      Keys.REGISTRATIONS);
  }

  getRegistrationById(id) {
    return this.getRegistrations().find(x => x.id === id);
  }

  register(registration, instructor, audit) {
    // TODO data validation
    registration.registrationType = RegistrationType.PRIVATE;
    const date = DateHelpers.getStartOfCurrentDayUTC();

    const rooms = [
      instructor.mondayRoomId,
      instructor.tuesdayRoomId,
      instructor.wednesdayRoomId,
      instructor.thursdayRoomId,
      instructor.fridayRoomId
    ];

    registration.roomId = rooms[registration.day * 1];

    registration.expectedStartDate = date;

    const weekDays = [
      'Monday',
      'Tuesday',
      'Wednesday',
      'Thursday',
      'Friday'
    ]

    const newRegistration =
      new Registration(
        0, // id will be generated by the database
        registration.studentId,
        registration.instructorId,
        weekDays[registration.day * 1],
        registration.startTime,
        registration.length,
        registration.registrationType,
        registration.roomId,
        registration.instrument,
        registration.transportationType,
        registration.notes,
        registration.expectedStartDate,
      );

    this.dbClient.appendRecord(Keys.REGISTRATIONS, newRegistration, audit);

    return newRegistration;
  }

  unregister(registrationId, audit) {
    try {
      this.dbClient.deleteRecord(Keys.REGISTRATIONS, registrationId, audit);
      return true;
    } catch {
      return false;
    }
  }
}
